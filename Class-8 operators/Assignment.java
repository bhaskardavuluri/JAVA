public class Assignment {
    public static void main(String[] args) {
        int x=10;
        System.out.println(x);
        
       x+=3;
       System.out.println(x);
       x-=3;
       System.out.println(x);
       x*=3;
       System.out.println(x);
       x/=3;
       System.out.println(x);
       x%=3;
       System.out.println(x);
       x&=3;
       System.out.println(x);
       x^=3;
       System.out.println(x);
       x>>=3;
       System.out.println(x);
       x<<=3;
       System.out.println(x);
    }
    
}
